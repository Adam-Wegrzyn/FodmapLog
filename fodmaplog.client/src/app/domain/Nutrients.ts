export class Nutrients{
    carbohydrates: number;
    protein: number;
    fat: number;
    fibre: number;

    constructor(carbohydrates: number, protein: number, fat: number, fibre: number){
        this.carbohydrates = carbohydrates;
        this.protein = protein;
        this.fat = fat;
        this.fibre = fibre;
    }
}